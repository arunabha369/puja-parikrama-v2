import { useState, useEffect } from 'react';
import { useNavigate, useLocation } from 'react-router-dom';
import { Flower, Mail, Lock, User, Github } from 'lucide-react';
import { usePuja } from '../context/PujaContext';

const Login = () => {
    const [isLogin, setIsLogin] = useState(true);
    const navigate = useNavigate();
    const location = useLocation();
    const { login, user } = usePuja();

    // specific colors from the detailed CSS request or fallback to theme
    // We will use the CSS variables injected into :root and .dark

    const toggleMode = () => setIsLogin(!isLogin);

    const handleAuth = () => {
        // Mock login
        login({ name: 'User', email: 'user@example.com' });
    };

    useEffect(() => {
        if (user) {
            // Redirect to the page they were trying to visit or planner by default
            const origin = location.state?.from?.pathname || '/planner';
            navigate(origin, { replace: true });
        }
    }, [user, navigate, location]);

    return (
        <div className="min-h-screen flex items-center justify-center px-4 pt-32 pb-4 relative z-20">
            {/* Back Button Removed as per request */}

            <div className="glass-panel w-full max-w-md p-8 rounded-2xl relative overflow-hidden animate-fade-in-up">
                {/* Header */}
                <div className="text-center mb-8">
                    <div className="flex justify-center mb-4">
                        <div className="p-3 rounded-full bg-white/5 border border-white/10">
                            <Flower className="w-8 h-8 text-yellow-400" />
                        </div>
                    </div>
                    <h2 className="text-3xl font-cinzel font-bold text-white mb-2 text-glow">
                        {isLogin ? 'Welcome Back' : 'Join the Circle'}
                    </h2>
                    <p className="text-white/60 font-light text-sm">
                        {isLogin
                            ? 'Enter your credentials to access your account'
                            : 'Create an account to save your parikrama plans'}
                    </p>
                </div>

                {/* Form */}
                <form className="space-y-4" onSubmit={(e) => { e.preventDefault(); handleAuth(); }}>
                    {!isLogin && (
                        <div className="space-y-2">
                            <label className="text-xs font-medium text-white/80 ml-1">Full Name</label>
                            <div className="relative">
                                <input
                                    type="text"
                                    placeholder="John Doe"
                                    className="w-full bg-white/5 border border-white/10 rounded-xl px-4 py-3 pl-10 text-white placeholder-white/30 focus:outline-none focus:border-yellow-400/50 focus:ring-1 focus:ring-yellow-400/50 transition-all text-sm"
                                />
                                <User className="w-4 h-4 text-white/40 absolute left-3 top-3.5" />
                            </div>
                        </div>
                    )}

                    <div className="space-y-2">
                        <label className="text-xs font-medium text-white/80 ml-1">Email</label>
                        <div className="relative">
                            <input
                                type="email"
                                placeholder="name@example.com"
                                className="w-full bg-white/5 border border-white/10 rounded-xl px-4 py-3 pl-10 text-white placeholder-white/30 focus:outline-none focus:border-yellow-400/50 focus:ring-1 focus:ring-yellow-400/50 transition-all text-sm"
                            />
                            <Mail className="w-4 h-4 text-white/40 absolute left-3 top-3.5" />
                        </div>
                    </div>

                    <div className="space-y-2">
                        <label className="text-xs font-medium text-white/80 ml-1">Password</label>
                        <div className="relative">
                            <input
                                type="password"
                                placeholder="••••••••"
                                className="w-full bg-white/5 border border-white/10 rounded-xl px-4 py-3 pl-10 text-white placeholder-white/30 focus:outline-none focus:border-yellow-400/50 focus:ring-1 focus:ring-yellow-400/50 transition-all text-sm"
                            />
                            <Lock className="w-4 h-4 text-white/40 absolute left-3 top-3.5" />
                        </div>
                    </div>

                    <button className="btn-liquid w-full mt-6">
                        {isLogin ? 'Sign In' : 'Create Account'}
                    </button>
                </form>

                {/* Divider */}
                <div className="relative my-6">
                    <div className="absolute inset-0 flex items-center">
                        <span className="w-full border-t border-white/10"></span>
                    </div>
                    <div className="relative flex justify-center text-xs uppercase">
                        <span className="bg-[#2e0202] px-2 text-white/40">Or continue with</span>
                    </div>
                </div>

                {/* Social Auth */}
                <div className="grid grid-cols-2 gap-4">
                    <button onClick={handleAuth} className="flex items-center justify-center gap-2 bg-white/5 hover:bg-white/10 border border-white/10 rounded-xl py-2.5 transition-colors text-white/80 text-sm font-medium">
                        <Github className="w-4 h-4" />
                        Github
                    </button>
                    <button onClick={handleAuth} className="flex items-center justify-center gap-2 bg-white/5 hover:bg-white/10 border border-white/10 rounded-xl py-2.5 transition-colors text-white/80 text-sm font-medium">
                        <svg className="w-4 h-4" viewBox="0 0 24 24" fill="currentColor">
                            <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4" />
                            <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
                            <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05" />
                            <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
                        </svg>
                        Google
                    </button>
                </div>

                {/* Footer */}
                <div className="mt-8 text-center text-sm text-white/60">
                    {isLogin ? "Don't have an account? " : "Already have an account? "}
                    <button
                        onClick={toggleMode}
                        className="text-yellow-400 hover:text-yellow-300 font-medium ml-1 transition-colors"
                    >
                        {isLogin ? 'Sign up' : 'Log in'}
                    </button>
                </div>
            </div>
        </div>
    );
};

export default Login;
